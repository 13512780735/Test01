package com.feemoo.network.model;

public class OnlineZipModel {

    /**
     * name : AZNQ0825.mp4
     * ext : mp4
     * size : 51.12 M
     */

    private String name;
    private String ext;
    private String size;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getExt() {
        return ext;
    }

    public void setExt(String ext) {
        this.ext = ext;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
